let provider;
let signer;
let tokenContract;
let userAddress;

// Connect to the wallet
document.getElementById("connectButton").addEventListener("click", async () => {
  if (window.ethereum) {
    provider = new ethers.providers.Web3Provider(window.ethereum);
    await provider.send("eth_requestAccounts", []);
    signer = provider.getSigner();
    userAddress = await signer.getAddress();
    
    document.getElementById("walletInfo").innerHTML = `Connected: ${userAddress}`;
    document.getElementById("balanceSection").style.display = "block";
    document.getElementById("transferSection").style.display = "block";

    tokenContract = new ethers.Contract(tokenAddress, tokenABI, signer);
  } else {
    alert("Please install MetaMask to use this feature.");
  }
});

// Check balance of the connected wallet
document.getElementById("checkBalanceButton").addEventListener("click", async () => {
  const balance = await tokenContract.balanceOf(userAddress);
  const formattedBalance = ethers.utils.formatUnits(balance, 18); // assuming 18 decimals for ERC-20
  document.getElementById("balanceDisplay").innerHTML = `Balance: ${formattedBalance} Tokens`;
});

// Transfer tokens
document.getElementById("transferButton").addEventListener("click", async () => {
  const recipientAddress = document.getElementById("recipientAddress").value;
  const amount = document.getElementById("amount").value;

  if (!ethers.utils.isAddress(recipientAddress)) {
    document.getElementById("transferStatus").innerHTML = "Invalid recipient address!";
    return;
  }

  try {
    const tx = await tokenContract.transfer(recipientAddress, ethers.utils.parseUnits(amount, 18));
    await tx.wait(); // wait for the transaction to be mined
    document.getElementById("transferStatus").innerHTML = `Transaction Successful! Tx Hash: ${tx.hash}`;
  } catch (error) {
    document.getElementById("transferStatus").innerHTML = `Error: ${error.message}`;
  }
});
